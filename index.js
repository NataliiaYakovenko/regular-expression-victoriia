//МЕТОДИ РЕГУЛЯРНИХ ВИРАЗІВ або методи з використанням регулярних виразів

// метод replace -  дозволяє замінити частину рядка, що відповідає певному шаблону регулярному виразу, на новий рядок.

const string1 = 'Rghghg              hjhjkhj                dfgfd          fghfgfhg';
