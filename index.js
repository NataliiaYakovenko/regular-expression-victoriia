console.log(string1Result);
console.log('//--------------------------------------------------------------------');

//Замінити ненормативну лексику з повідомлень (fuck, ass, asshole)
const string2 = 'Fuck you. ))))))) You are asshole';
const string2Result = string2.replace(/(fuck|ass|asshole)\b/gi,'!@#$')
console.log(string2);
console.log(string2Result);
console.log('//--------------------------------------------------------------------');

const string3 ='Fuck you dude! AAAAAASSHOLE!! FuCCCKKK Fuck Fuck Fuck Fuck Fuck!!!';
